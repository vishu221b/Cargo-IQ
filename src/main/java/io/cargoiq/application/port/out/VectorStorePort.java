package io.cargoiq.application.port.out;

import io.cargoiq.domain.model.Citation;
import io.cargoiq.domain.model.DocumentChunk;
import io.cargoiq.domain.model.DocumentType;
import io.cargoiq.domain.model.Incoterm;

import java.util.List;
import java.util.Optional;

/**
 * Outbound port: similarity-search over embedded chunks.
 *
 * <p>Deliberately phrased in domain terms, not in Spring AI terms — the
 * application asks for {@link Citation}s back, not {@code Document<Float>}.
 * The pgvector adapter does the translation. If we ever swap pgvector for
 * Qdrant or Pinecone, only the adapter changes.
 *
 * <p>Embedding generation is the adapter's responsibility — the application
 * sends raw text and trusts the adapter to embed-and-search with whatever
 * EmbeddingModel is wired.
 */
public interface VectorStorePort {

    /**
     * Index a batch of chunks. Embeddings are generated by the adapter.
     */
    void index(List<DocumentChunk> chunks, ChunkMetadata metadata);

    /**
     * Retrieve the topK most similar chunks for the given query.
     */
    List<Citation> similaritySearch(SearchRequest request);

    void deleteByDocumentId(java.util.UUID documentId);

    /** Filters carried alongside the embedding when indexing — used at retrieval time. */
    record ChunkMetadata(
            java.util.UUID documentId,
            String documentTitle,
            DocumentType type,
            Optional<Incoterm> incoterm) {}

    record SearchRequest(
            String queryText,
            int topK,
            Optional<DocumentType> filterByType,
            Optional<Incoterm> filterByIncoterm) {}
}
